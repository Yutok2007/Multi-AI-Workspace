export type SupportedLocale = keyof typeof messages;
export type MessageKey = keyof (typeof messages)['en'];

export function resolveLocale(
  preference: LocalePreference,
  browserLocale = globalThis.navigator?.language ?? 'en',
): SupportedLocale {
  if (preference !== 'browser') {
    return preference;
  }
  const normalized = browserLocale.toLowerCase();
  if (normalized.startsWith('zh-tw') || normalized.startsWith('zh-hk')) {
    return 'zh-TW';
  }
  if (normalized.startsWith('zh')) {
    return 'zh-CN';
  }
  return 'en';
}

export function createTranslator(locale: SupportedLocale) {
  return (key: MessageKey, values: Record<string, string | number> = {}): string => {
    const template: string = messages[locale][key] ?? messages.en[key];
    return Object.entries(values).reduce(
      (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
      template,
    );
  };
}
